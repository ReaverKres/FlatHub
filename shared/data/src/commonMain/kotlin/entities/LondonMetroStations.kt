    fun lineForStationName(name: String): MetroLine? {
        val n = name.lowercase()
        if (BAKERLOO_LINE.any { it.name.lowercase() == n }) return BAKERLOO_LINE.first().line
        if (CENTRAL_LINE.any { it.name.lowercase() == n }) return CENTRAL_LINE.first().line
        if (CIRCLE_LINE.any { it.name.lowercase() == n }) return CIRCLE_LINE.first().line
        if (DISTRICT_LINE.any { it.name.lowercase() == n }) return DISTRICT_LINE.first().line
        if (HAMMERSMITH_CITY_LINE.any { it.name.lowercase() == n }) return HAMMERSMITH_CITY_LINE.first().line
        if (JUBILEE_LINE.any { it.name.lowercase() == n }) return JUBILEE_LINE.first().line
        if (METROPOLITAN_LINE.any { it.name.lowercase() == n }) return METROPOLITAN_LINE.first().line
        if (NORTHERN_LINE.any { it.name.lowercase() == n }) return NORTHERN_LINE.first().line
        if (PICCADILLY_LINE.any { it.name.lowercase() == n }) return PICCADILLY_LINE.first().line
        if (VICTORIA_LINE.any { it.name.lowercase() == n }) return VICTORIA_LINE.first().line
        if (WATERLOO_CITY_LINE.any { it.name.lowercase() == n }) return WATERLOO_CITY_LINE.first().line
        if (ELIZABETH_LINE.any { it.name.lowercase() == n }) return ELIZABETH_LINE.first().line
        return null
    }

